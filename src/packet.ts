export function crc16(data: Buffer): number {
  let cksum = 0;
  for (let i = 0; i < data.length; i++) {
    cksum = (CRC16_TABLE[((cksum >> 8) ^ data[i]) & 0xff] ^ (cksum << 8)) & 0xffff;
  }
  return cksum;
}

// Build a framed VESC packet from a payload buffer.
// Frame: [start_byte][len bytes][payload][crc_hi][crc_lo][0x03]
// start_byte=2 for payload <=255 bytes (1-byte length), 3 for <=65535 (2-byte length).
export function buildPacket(payload: Buffer): Buffer {
  const len = payload.length;
  const headerSize = len <= 255 ? 2 : 3;
  const frame = Buffer.allocUnsafe(headerSize + len + 3);
  let i = 0;
  if (len <= 255) {
    frame[i++] = 2;
    frame[i++] = len;
  } else {
    frame[i++] = 3;
    frame[i++] = (len >> 8) & 0xff;
    frame[i++] = len & 0xff;
  }
  payload.copy(frame, i);
  i += len;
  const crc = crc16(payload);
  frame[i++] = (crc >> 8) & 0xff;
  frame[i++] = crc & 0xff;
  frame[i++] = 0x03;
  return frame;
}

// Stateful packet decoder. Feed raw serial bytes via feed().
// Emits 'packet' events with the decoded payload Buffer.
export class PacketDecoder extends EventEmitter {
  private buf: Buffer = Buffer.alloc(0);

  feed(data: Buffer): void {
    this.buf = Buffer.concat([this.buf, data]);
    this.drain();
  }

  private drain(): void {
    for (;;) {
      const result = this.tryDecodeOne();
      if (result === null) break;       // need more data
      if (result > 0) {
        this.buf = this.buf.slice(result); // consumed result bytes
      } else {
        this.buf = this.buf.slice(1);     // invalid start byte, skip
      }
    }
  }

  // Returns:
  //   >0  — number of bytes consumed (packet decoded and emitted)
  //   -1  — invalid byte at position 0, caller should skip it
  //   null — need more data
  private tryDecodeOne(): number | null {
    const buf = this.buf;
    if (buf.length === 0) return null;

    const startByte = buf[0];
    if (startByte !== 2 && startByte !== 3) return -1;

    // start byte doubles as header size (2 or 3 bytes)
    const headerSize = startByte;
    if (buf.length < headerSize) return null;

    let payloadLen: number;
    if (startByte === 2) {
      payloadLen = buf[1];
      if (payloadLen < 1) return -1;
    } else {
      payloadLen = (buf[1] << 8) | buf[2];
      if (payloadLen < 256) return -1; // must use 8-bit length for small packets
    }

    if (payloadLen > 512) return -1; // exceeds PACKET_MAX_PL_LEN

    // header + payload + 2 CRC bytes + 1 end byte
    const totalLen = headerSize + payloadLen + 3;
    if (buf.length < totalLen) return null;

    // end byte must be 0x03
    if (buf[headerSize + payloadLen + 2] !== 0x03) return -1;

    const payload = buf.slice(headerSize, headerSize + payloadLen);
    const crcCalc = crc16(payload);
    const crcRx = (buf[headerSize + payloadLen] << 8) | buf[headerSize + payloadLen + 1];
    if (crcCalc !== crcRx) return -1;

    this.emit('packet', Buffer.from(payload)); // copy so caller owns the buffer
    return totalLen;
  }
}
